    <h2>ライフル銃の種類</h2>
    <p>ライフル銃にはいくつかの方式があります。</p>
    <ul class="info-list">
      <li><strong>ボルトアクション式：</strong>最も精度が高く、猟銃として一般的。</li>
      <li><strong>セミオート式：</strong>1発ずつ自動装填。反動制御・規制に注意。</li>
      <li><strong>レバーアクション式：</strong>操作性が良く、狩猟で使われることもある。</li>
    </ul>

    <h2>ライフル弾の特徴</h2>
    <p>ライフル弾は1つの重い弾頭を高速で発射するため、散弾銃の「散弾」とは大きく異なります。</p>
