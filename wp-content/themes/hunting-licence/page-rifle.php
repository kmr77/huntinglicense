    <h2>ライフル銃の構造と仕組み</h2>
    <p>ライフル銃の最大の特徴は、銃身内部の<strong>ライフリング（旋条）</strong>です。この溝が弾丸に回転を与え、以下の効果を生みます。</p>
    <ul class="info-list">
      <li><strong>直進性が極めて高くなる</strong>（空気抵抗の影響を受けにくい）</li>
      <li><strong>遠距離でも軌道が安定</strong>し、狙った位置に着弾しやすい</li>
      <li><strong>大型獣に有効な貫通力</strong>を持つ</li>
    </ul>
    <p>そのため、ライフル銃は<strong>精密射撃用の銃</strong>として位置付けられています。</p>
