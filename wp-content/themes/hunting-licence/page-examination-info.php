    <h2 id="faq">よくある質問（FAQ）</h2>
    <section class="tag-faq">
      <dl>
        <dt>Q. 猟銃等講習会の受講資格は？</dt>
        <dd>A. 原則として満20歳以上で、欠格事由に該当しない方が対象です。</dd>
        <dt>Q. どのくらいの頻度で開催されていますか？</dt>
        <dd>A. 都道府県ごとに年数回、主に春・秋に開催されます。</dd>
        <dt>Q. 申請に必要な書類は？</dt>
        <dd>A. 住民票、写真、申請書、診断書などが必要です。</dd>
        <dt>Q. 講習会の内容はどんなものですか？</dt>
        <dd>A. 銃の構造・安全管理・法令の座学と筆記試験が中心です。</dd>
        <dt>Q. 筆記試験の合格点は？</dt>
        <dd>A. 通常20問中16問（80％）以上正解で合格となります。</dd>
        <dt>Q. 実技試験はありますか？</dt>
        <dd>A. 地域によっては射撃の安全操作や技能チェックがあります。</dd>
        <dt>Q. 合格証明書の有効期限は？</dt>
        <dd>A. 修了証明書の有効期限は通常3年です。</dd>
        <dt>Q. 再受講・再試験はできますか？</dt>
        <dd>A. 不合格の場合も再度受講・受験が可能です。</dd>
        <dt>Q. 合格後の流れは？</dt>
        <dd>A. 猟銃所持許可申請や登録手続きを行います。</dd>
        <dt>Q. 独学でも合格できますか？</dt>
        <dd>A. 重要ポイントを押さえて過去問を反復すれば独学でも十分合格可能です。</dd>
      </dl>
