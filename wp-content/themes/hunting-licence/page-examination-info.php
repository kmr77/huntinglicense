    <h2 id="about">猟銃等講習会とは？</h2>
    <p>
      猟銃免許（猟銃等講習会）は、銃を安全に所持・使用するために必須の資格試験です。<br>
      公安委員会が主催し、銃の構造・安全管理・法令・射撃の実技を学びます。<br>
      合格者には「猟銃等講習会修了証明書」が交付され、所持許可の申請が可能となります。
    </p>

    <h2 id="flow">受講の流れ・日程</h2>
    <ol>
      <li>警察署や公安委員会にて受講申請（必要書類提出）</li>
      <li>指定日に講習会を受講</li>
      <li>筆記試験（銃の構造・安全管理・法令等）</li>
      <li>合格後、修了証明書を受領</li>
      <li>猟銃所持許可申請など次のステップへ</li>
    </ol>
    <p>
      <a href="/information/">全国の講習会日程はこちら</a>
    </p>

    <h2 id="content">試験の内容と合格基準</h2>
    <ul>
      <li>【筆記試験】 銃の構造・安全管理・銃刀法等の法令問題（択一式＋記述式）</li>
      <li>【合格基準】 正答率80％程度が目安。安全管理・法令知識が重視されます。</li>
      <li>【実技】 地域によっては射撃技術の確認あり</li>
    </ul>

