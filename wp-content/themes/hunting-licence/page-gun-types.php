    <h2>よくある質問</h2>
    <dl>
      <dt>Q. 散弾銃とライフル銃の違いは？</dt>
      <dd>→ 散弾銃は複数の小さな弾を広範囲に発射し、鳥類や近距離の獣に有効です。ライフル銃は単発の弾を高精度で遠距離に撃てるため、大型獣向けです。</dd>

      <dt>Q. ハーフライフル銃とは何ですか？</dt>
      <dd>→ 銃身の一部に旋条（ライフリング）がある銃で、散弾銃とライフル銃の中間的性能を持ちます。2025年からライフル扱いとなりました。</dd>

      <dt>Q. 初心者にはどの銃がおすすめ？</dt>
      <dd>→ 散弾銃（中折れ式や自動銃）が最も入門向きです。構造が比較的シンプルで、講習や所持許可もライフルより取得しやすいです。</dd>

      <dt>Q. 空気銃は銃猟免許が必要ですか？</dt>
      <dd>→ はい。第二種銃猟免許が必要で、警察の所持許可も必要です。小動物向けの軽量な銃として使用されます。</dd>

      <dt>Q. 銃の所持に必要な条件は？</dt>
      <dd>→ 20歳以上で、適性検査・身辺調査・講習受講・射撃試験などをクリアする必要があります。詳細は警察署で確認しましょう。</dd>

      <dt>Q. 銃は自宅に保管できますか？</dt>
      <dd>→ はい。ただし厳重な保管が義務付けられており、施錠された銃保管庫と実包保管庫が必要です。警察の家庭訪問で保管状況が確認されます。</dd>

      <dt>Q. 猟銃所持者になるにはどれくらい時間がかかりますか？</dt>
      <dd>→ 初めての申請から所持許可まで3〜6ヶ月程度かかります。書類準備、講習、身辺調査、技術講習などの工程が複数あるためです。</dd>

      <dt>Q. 自作の銃は合法ですか？</dt>
      <dd>→ いいえ、銃砲刀剣類所持等取締法により自作銃の所持・製造は厳しく禁止されています。違反すれば重い刑罰が科されます。</dd>

      <dt>Q. ライフル所持は誰でもできますか？</dt>
      <dd>→ 原則として散弾銃の所持歴が10年以上ある者に限られます。猟歴・射撃実績・猟友会の推薦などが必要です。</dd>

      <dt>Q. ハーフライフルは今でも取得可能ですか？</dt>
      <dd>→ 2025年の法改正でライフル扱いとなったため、取得にはライフルと同様の基準が適用されます。北海道では特例措置もあります。</dd>
    </dl>
