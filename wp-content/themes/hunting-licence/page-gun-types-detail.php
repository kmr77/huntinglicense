    <h2>有効射程距離と最高到達距離の違い</h2>
    <p>
      狩猟読本などで示される射程距離は「有効射程距離」を指します。
      これは安全かつ確実に狩猟が行える距離であり、弾が届く最大距離（最高到達距離）とは異なります。
    </p>
    <p>
      特にライフル銃やハーフライフル銃では、有効射程を大きく超えて弾が飛翔する可能性があるため、
      発砲時には背景・地形・安全方向の確認が不可欠です。
    </p>

    <h2>銃の種類ごとに所持条件が異なる理由</h2>
