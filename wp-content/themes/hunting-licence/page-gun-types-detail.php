    <h2>よくある質問（FAQ）</h2>
    <dl>
      <dt>Q. 読本に書かれている射程距離は絶対的な数値ですか？</dt>
      <dd>A. いいえ。一般的な条件を想定した目安であり、実際の射程は状況によって変わります。</dd>

      <dt>Q. 有効射程距離と最高到達距離の違いは？</dt>
      <dd>A. 有効射程は安全かつ確実に使用できる距離、最高到達距離は弾が理論上届く最大距離です。</dd>

      <dt>Q. ハーフライフルとライフルの狩猟方法は同じですか？</dt>
      <dd>A. はい。どちらも主に待ち猟や忍び猟で用いられます。</dd>

      <dt>Q. 散弾銃で大型獣を狩猟できますか？</dt>
      <dd>A. 地域や規制、弾種によって異なりますが、一般的にはライフル銃が用いられることが多いです。</dd>

      <dt>Q. なぜライフル銃の所持条件は厳しいのですか？</dt>
      <dd>A. 有効射程が長く、社会的安全への影響が大きいためです。</dd>

      <dt>Q. 初心者は射程距離だけで銃を選んでよいですか？</dt>
      <dd>A. おすすめできません。狩猟方法や所持条件、安全管理を総合的に考える必要があります。</dd>

      <dt>Q. 射程距離は試験に出ますか？</dt>
      <dd>A. 数値そのものよりも、有効射程と安全管理の考え方が問われることが多いです。</dd>

      <dt>Q. ハーフライフルは今後さらに規制されますか？</dt>
      <dd>A. 法改正の可能性はあります。最新情報は必ず公式発表を確認してください。</dd>

      <dt>Q. 銃の所持条件は全国共通ですか？</dt>
