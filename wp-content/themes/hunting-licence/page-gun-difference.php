    <h2>よくある質問</h2>
    <dl>
      <dt>Q. 散弾銃と空気銃の費用の違いは？</dt>
      <dd>→ 散弾銃は銃本体や弾薬代、ロッカーなどを含め20万〜30万円以上かかることが多いです。空気銃は数万円〜10万円程度で始められます。</dd>

      <dt>Q. どちらの銃が初心者向けですか？</dt>
      <dd>→ 空気銃は反動が少なく、射撃音も小さいため初心者には扱いやすいとされています。ただし、所持許可などの手続きはどちらも同様です。</dd>

      <dt>Q. 空気銃は狩猟に使えますか？</dt>
      <dd>→ はい。キジバトやヒヨドリなどの鳥類、小型哺乳類の狩猟に使われています。ただし、シカやイノシシなど大型獣には使用できません。</dd>

      <dt>Q. 散弾銃はどんな場面で使われますか？</dt>
      <dd>→ 鳥類の飛翔中の射撃（流し猟）や、イノシシ・シカなどの中大型獣の至近距離での狩猟に使用されます。威力が高く、取り扱いには十分な注意が必要です。</dd>

      <dt>Q. 所持のための手続きに違いはありますか？</dt>
      <dd>→ 手続きの基本的な流れは同じで、どちらも銃所持許可が必要です。公安委員会での講習、試験、身辺調査、保管設備などが求められます。</dd>

      <dt>Q. 空気銃と散弾銃で試験内容は違いますか？</dt>
      <dd>→ 筆記試験は共通ですが、実技では扱う銃器が異なるため、取り扱い方法の実演内容に違いがあります。</dd>

      <dt>Q. 反動や音に違いはありますか？</dt>
      <dd>→ 散弾銃は火薬を使用するため反動と発射音が大きく、空気銃は反動も音も比較的小さいです。</dd>

      <dt>Q. 空気銃はどれくらいの距離まで有効ですか？</dt>
      <dd>→ 空気銃は有効射程が30〜50メートル程度とされ、狙撃精度を要する場面に向いています。</dd>

      <dt>Q. 法律上の扱いは違いますか？</dt>
      <dd>→ どちらも銃刀法の対象で、所持・運搬・使用には厳しい制限があります。空気銃も玩具とは異なり、正式な銃器として扱われます。</dd>

      <dt>Q. 両方を所持することは可能ですか？</dt>
      <dd>→ はい。所持許可をそれぞれ申請・取得すれば、散弾銃と空気銃の両方を所持することが可能です。</dd>
    </dl>

