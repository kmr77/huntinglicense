    <h2>散弾銃とは？</h2>
    <p>散弾銃は、火薬を使用して複数の小さな弾（散弾）を一度に発射する猟銃です。</p>
    <p>射程が広く、動く標的に命中させやすい特性を持ち、鳥類だけでなくシカやイノシシなどの大型獣にも対応できます。</p>

    <h2>空気銃とは？</h2>
    <p>空気銃は、圧縮空気を使って単発の弾丸を発射する銃です。</p>
    <p>火薬を使わないため静かで反動が小さく、扱いやすいのが特徴です。主に鳥類など小型の動物を対象とした猟に使われます。</p>

