    <h2>狩猟免許試験に関する質問</h2>

    <h3><span class="marker">Q. 狩猟免許のテスト内容は？</span></h3>
    <p>A. 狩猟免許試験の内容は、狩猟の種類によって異なりますが、共通して法令に関する筆記試験、猟具の構造・取扱い、鳥獣の識別、安全管理に関する知識を問う筆記試験、さらには実技試験が実施されます。わな猟や網猟では実際の設置や取り扱い方法が実技として出題されます。詳細は<a href="https://www.shuryo-menkyo.com/content/">狩猟免許試験の内容と対策ページ</a>をご覧ください。</p>

    <h3><span class="marker">Q. 狩猟免許の合格ラインは？</span></h3>
    <p>A. 合格ラインは都道府県によって若干異なりますが、一般的に筆記試験では7割以上の正答率（35問中24問以上正解）で合格となります。実技試験においては重大なミスがあった場合は不合格となることがあります。詳細な基準は各都道府県の試験要項をご確認ください。</p>

    <h3><span class="marker">Q. 狩猟免許のわなの合格率は？</span></h3>
    <p>A. わな猟免許（わな猟狩猟免許）は比較的取得しやすく、各県で8割前後の合格率が報告されています。筆記は比較的平易ですが、実技試験では安全確認やわなの設置手順などの基本ができていないと減点されます。油断せず基本を徹底することが重要です。</p>

    <h3><span class="marker">Q. 狩猟免許試験の勉強時間はどのくらいですか？</span></h3>
    <p>A. 独学で受験する場合、1日1〜2時間の学習を2〜3週間続ける方が多いです。効率よく学習するには、過去問を活用し、実技講習や猟友会の予備講習に参加するのが効果的です。短期集中で対策する方法については<a href="https://www.shuryo-menkyo.com/study-method/">こちらの勉強方法ページ</a>もご参考ください。</p>

    <h3><span class="marker">Q. 狩猟免許を取れない人は？</span></h3>
    <p>A. 以下の方は狩猟免許の取得が制限されます：<br>
    ・銃刀法違反や禁錮刑以上の前科がある者<br>
    ・精神疾患により判断力が著しく低下している者<br>
    ・麻薬・アルコール依存症と診断されている者<br>
    ・暴力団関係者など<br>
    銃猟免許の場合は、さらに厳しい適性検査（視力・聴力・運動能力など）があります。</p>

    <h3><span class="marker">Q. 狩猟免許試験は年に何回実施されますか？</span></h3>
    <p>A. 狩猟免許試験は都道府県によって異なりますが、一般的に年2回（前期・後期）実施されます。一部の県では年3回以上実施される場合もあります。申し込み時期や日程は各自治体のホームページで確認が必要です。</p>

    <h3><span class="marker">Q. 試験当日の持ち物は何ですか？</span></h3>
    <p>A. 狩猟免許試験当日は、受験票、筆記用具（黒のボールペンなど）、身分証明書（運転免許証など）、受験料（事前納付の場合もあり）を持参する必要があります。実技がある場合は動きやすい服装・運動靴が推奨されます。</p>

    <h3><span class="marker">Q. 免許の有効期限は？更新手続きは必要ですか？</span></h3>
    <p>A. 狩猟免許の有効期限は3年間です。期限が近づいたら更新講習を受け、更新手続きを行う必要があります。更新講習を受けないまま期限を過ぎると、再度新規で受験する必要があります。</p>

    <h2>費用・収入・条件に関する質問</h2>

    <h3><span class="marker">Q. 狩猟免許を取るのにかかる費用は？</span></h3>
    <p>A. 試験申請料・講習費・教材費などを含めて、網猟・わな猟では約1〜2万円、銃猟免許ではさらに猟銃所持許可や実技講習料を含めて約7〜10万円かかることもあります。詳しくは<a href="https://www.shuryo-menkyo.com/application/">狩猟免許の申請・費用ページ</a>をご覧ください。</p>

    <h3><span class="marker">Q. 猟師の平均年収はいくらですか？</span></h3>
    <p>A. 専業猟師として生計を立てている人は少数で、多くは副業や地域貢献として活動しています。有害鳥獣駆除や行政委託による報酬を含めて、年間10万円〜100万円程度の副収入を得ている人が多いとされます。</p>

    <h3><span class="marker">Q. ハンターになれる条件は？</span></h3>
    <p>A. 狩猟免許を取得し、都道府県への「狩猟者登録」を行うことでハンターとして活動できます。年齢制限は以下の通りです：<br>
    ・わな猟・網猟 → 満18歳以上<br>
    ・銃猟免許（第一種・第二種） → 満20歳以上<br>
    身体的・精神的な適性が必要です。詳細は<a href="https://www.shuryo-menkyo.com/application/">狩猟免許申請ガイド</a>をご覧ください。</p>

    <h3><span class="marker">Q. ヒグマハンターになるには？</span></h3>
    <p>A. 銃猟免許（第一種）とライフル所持許可が必要です。北海道などでは特例措置が設けられており、捕獲従事者や認定者であればハーフライフル所持が許可される場合もあります。法改正の影響など詳しくは<a href="https://www.shuryo-menkyo.com/rifle-revision/">2025年法改正のページ</a>をご覧ください。</p>

    <h3><span class="marker">Q. 罠猟は難しいですか？</span></h3>
    <p>A. 罠猟は設置場所の見極め、鳥獣の習性理解、定期的な見回りなど実践的な要素が多く含まれます。慣れないうちは難しく感じることもありますが、経験を積めば確実に上達できます。猟友会や講習会で基礎を学ぶのがおすすめです。</p>

    <h3><span class="marker">Q. 狩猟免許なしで捕獲するとどうなる？</span></h3>
    <p>A. 鳥獣保護管理法に違反し、無免許で捕獲行為を行った場合、懲役・罰金などの刑事罰を受ける可能性があります。たとえ害獣駆除目的でも、正規の免許と登録手続きが必要です。</p>

