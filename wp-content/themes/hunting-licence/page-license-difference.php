			<li>わな猟免許：くくりわな・箱わななどを使った狩猟</li>
			<li>網猟免許：落とし網・むそう網などを使った狩猟</li>
		</ul>
		<p>これらは「どの方法で狩猟するか」によって分類されており、実技試験や筆記試験で狩猟に関する法律・知識が問われます。ただし、狩猟免許を取得しても銃を購入・所持することはできません。</p>

		<h2>銃の所持には別の許可が必要</h2>
		<p>実際に猟銃（例：散弾銃、ライフル、空気銃など）を所持するには、警察の発行する「銃所持許可」が必要です。これは狩猟免許とは独立した制度であり、所持する銃ごとに1丁ずつ許可を取得する必要があります。</p>
		<p>銃所持の手続きには、以下のような流れがあります：</p>
		<ol class="info-list">
