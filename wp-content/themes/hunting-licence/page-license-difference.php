			<li>精神科医による診断書の提出</li>
			<li>身辺調査（前科・交友関係・家庭環境など）</li>
			<li>公安委員会による面接・判断</li>
			<li>技能講習・実技試験（射撃場での訓練）</li>
		</ol>
