		<h2>狩猟免許とは？</h2>
		<p>狩猟免許は、野生鳥獣の捕獲を目的とした「狩猟行為」を行うために必要な資格です。種類は第一種銃猟・第二種銃猟・罠猟・網猟の4つがあります。これは「狩猟ができるかどうか」を定める免許であり、銃の所持そのものとは関係ありません。</p>

		<h2>銃の所持には別の許可が必要</h2>
		<p>実際に猟銃（散弾銃や空気銃など）を所持するには、別途「銃の所持許可」が必要です。この許可を得るには、猟銃等講習会の受講・考査合格・警察による身辺調査・精神鑑定・安全講習・技能講習など、厳格な手続きがあります。</p>

		<h2>猟銃等講習会とは？</h2>
		<p>銃の所持許可を得るための最初のステップが「猟銃等講習会」です。講習では安全管理・法律・構造などを学び、終了後には筆記試験（考査）があります。これが一般に「銃免許試験」と呼ばれることもあります。</p>

		<h2>狩猟免許と銃所持許可の違いまとめ</h2>
