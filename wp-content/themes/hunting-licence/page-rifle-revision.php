    <h2>よくある質問（Q&A）</h2>
    <dl>
      <dt>Q. なぜ2025年に銃刀法が改正されたのですか？</dt>
      <dd>→ 近年の銃器による事件（例：2023年の中野市事件など）を受け、銃器の安全管理や所持条件の見直しが必要と判断されたためです。特にハーフライフル銃の位置付けが見直されました。</dd>

      <dt>Q. ハーフライフル銃とは何ですか？</dt>
      <dd>→ 銃身の一部に旋条（ライフリング）が施された銃で、ライフルと散弾銃の中間的な性能を持ちます。これまで所持要件が緩やかでしたが、法改正でライフルと同等の扱いになりました。</dd>

      <dt>Q. 法改正によって何が変わりましたか？</dt>
      <dd>→ 銃身の5分の1以上に旋条がある銃はライフル扱いとなり、所持には原則10年以上の散弾銃所持歴が必要になりました。新たな取得には実績証明も必要です。</dd>

      <dt>Q. 所持していたハーフライフルはどうなりますか？</dt>
      <dd>→ 経過措置により、既存の所持者は継続使用が可能ですが、更新や買い替え時にはライフル基準が適用されます。</dd>

