    <h2>よくある質問（Q&A）</h2>
    <dl>
      <dt>Q. 初年度にかかる総費用はいくら？</dt><dd>銃の種類や地域、準備内容によりますが、およそ15万〜25万円が目安です。</dd>
      <dt>Q. 所持許可の更新時にもお金がかかる？</dt><dd>はい。3年ごとの更新には手数料や診断書取得で約5,000〜10,000円程度かかります。</dd>
      <dt>Q. 講習会と試験は別料金ですか？</dt><dd>基本的にセットになっていることが多いですが、地域により別途費用がかかる場合があります。</dd>
      <dt>Q. 猟友会に入ると費用は安くなりますか？</dt><dd>講習や保険がパッケージ化されてお得な地域もあります。</dd>
      <dt>Q. 実銃は中古でも大丈夫ですか？</dt><dd>法的に問題はありませんが、状態の良いものを信頼できる銃砲店で選ぶことが重要です。</dd>
      <dt>Q. 銃ロッカーは自作してもよい？</dt><dd>公安委員会の基準を満たす必要があるため、市販品を推奨します。</dd>
      <dt>Q. 空気銃と散弾銃で費用は違う？</dt><dd>はい。空気銃の方が本体価格や教習費用がやや安い傾向があります。</dd>
      <dt>Q. 所持申請にかかる日数は？</dt><dd>講習修了後、申請から許可まではおよそ1〜2ヶ月が一般的です。</dd>
      <dt>Q. 家族の同意は必要？</dt><dd>多くの都道府県で家族の同意書が求められます。</dd>
      <dt>Q. 女性でも猟銃所持は可能ですか？</dt><dd>もちろん可能です。特に空気銃などは軽量で扱いやすく、女性猟師も増えています。</dd>
    </dl>

