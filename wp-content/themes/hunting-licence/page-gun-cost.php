    <h2>猟銃の保管方法と費用</h2>
    <p>猟銃の所持には、法律基準を満たす<strong>ガンロッカーと装弾ロッカー</strong>の設置が義務付けられています（警察による実地確認あり）。</p>
    <p>価格の目安は新品で<strong>3万〜5万円程度</strong>。中古や地域の補助金で抑えられることもありますが、基準を満たさない自作は認められにくいため<strong>市販品推奨</strong>です。</p>
    <ul class="list-style">
      <li>設置場所：家族が触れない施錠スペース／地震対策（アンカー固定等）が望ましい</li>
      <li>点検・鍵管理：ダブルロック・鍵の別保管が基本</li>
    </ul>

