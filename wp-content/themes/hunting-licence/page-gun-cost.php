    <h2>猟銃所持許可の更新と費用</h2>
    <p>猟銃の所持許可は<strong>3年ごとの更新</strong>が必要で、次のような費用がかかります：</p>
    <ul class="list-style">
      <li>更新手数料：約6,000円</li>
      <li>再度の診断書取得費用：5,000〜10,000円</li>
      <li>講習（技能講習）の受講料：3,000〜5,000円</li>
    </ul>
    <p>更新手続きが遅れると許可が失効するため、スケジュールには余裕を持って準備しましょう。</p>

    <h2>地域による費用差と助成金制度</h2>
    <p>猟銃所持にかかる費用は<strong>都道府県ごとに差</strong>があり、一部自治体では講習会費用や診断書取得に対する補助金制度もあります。</p>
    <ul class="list-style">
      <li>例：北海道の一部地域では、猟友会主催の講習会費用が無料化されている場合あり</li>
      <li>市町村単位での支援もあり（移住者向け施策など）</li>
    </ul>
    <p>申請前にお住まいの地域の<a href="https://www.pref.hokkaido.lg.jp/" target="_blank" rel="noopener">公安委員会や猟友会</a>に確認するのがおすすめです。</p>

    <h2>2025年法改正：ハーフライフルがライフル扱いに</h2>
    <p>2025年3月の法改正により、<strong>ハーフライフル銃は「ライフル銃」として扱われる</strong>ようになりました。</p>
    <p>これにより、従来のハーフライフルと散弾銃の区別が変わり、<strong>許可要件・申請費用・講習内容も変更される可能性</strong>があります。詳細は各公安委員会に確認してください。</p>

