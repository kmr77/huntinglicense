    <h2>よくある質問（Q&A）</h2>
    <dl>
      <dt>Q. 猟銃保険は必ず入らなければいけませんか？</dt>
      <dd>法律上の義務ではありませんが、狩猟や射撃を行う場合は加入が強く推奨されています。多くの猟友会では加入が前提です。</dd>

      <dt>Q. 火災保険や自動車保険の特約で代用できますか？</dt>
      <dd>一部の個人賠償責任保険で補償対象となる場合がありますが、銃の使用を明記していない契約では補償されないことがあります。</dd>

      <dt>Q. 年間保険料はいくらくらい？</dt>
      <dd>補償範囲にもよりますが、年間3,000〜15,000円前後が一般的です。</dd>

      <dt>Q. 万一の事故で保険が使えないケースは？</dt>
      <dd>飲酒・無許可での所持・法令違反による事故は対象外となります。</dd>

      <dt>Q. 猟友会の保険と個人保険は併用可能？</dt>
      <dd>はい、二重加入しても問題ありません。狩猟中・日常生活中の両方をカバーできます。</dd>
    </dl>

