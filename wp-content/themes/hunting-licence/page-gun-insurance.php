    <h2>加入できる保険の種類</h2>
    <p>代表的な加入方法は以下の3つです。</p>
    <ul class="list-style">
      <li><strong>① 猟友会経由のハンター保険</strong>（団体契約・手続き簡単）</li>
      <li><strong>② 火災保険・自動車保険付帯の個人賠償責任保険</strong></li>
      <li><strong>③ 射撃専用のスポーツ保険</strong>（競技者向け）</li>
    </ul>
    <p>それぞれ補償範囲や上限金額が異なります。特に狩猟を伴う場合は、<strong>猟友会保険＋個人賠償保険の併用</strong>が安心です。</p>

    <h2>加入の流れ</h2>
    <ol class="list-style">
      <li>① 猟友会・保険代理店・保険会社のいずれかに申し込む</li>
      <li>② 保険プラン（補償金額・自己負担額）を選択</li>
      <li>③ 年払いで保険料を支払い（口座振替・現金払いなど）</li>
      <li>④ 保険証書・会員証を携帯し、狩猟登録時に提示</li>
    </ol>

