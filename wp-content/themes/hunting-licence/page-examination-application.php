    <h2>必要書類一覧と解説</h2>
    <ul>
      <li><strong>猟銃等講習会受講申込書</strong><br>
        各県指定のフォーマット。公式サイトまたは警察署で入手。</li>
      <li><strong>顔写真（縦3cm × 横2.4cm程度）</strong><br>
        6か月以内撮影、無帽・正面。パスポート用写真で代用可。</li>
      <li><strong>本人確認書類</strong><br>
        運転免許証やマイナンバーカードなど有効期限内のもの。</li>
      <li><strong>受講手数料（3,000〜4,200円程度）</strong><br>
        都道府県により異なり、現金または収入証紙での支払いが多い。</li>
      <li><strong>医師の診断書（精神障害や薬物依存の有無）</strong><br>
        群馬県・鹿児島県など一部で事前提出が必要。必要ない県もあるが、後の申請にも使うため早めの取得がおすすめ。</li>
    </ul>

    <h2>申込時の注意点とアドバイス</h2>
    <ul>
      <li>申込受付は講習日からおおむね2〜3週間前に締切</li>
      <li>書類の記入ミスや写真サイズ違いが多いので注意</li>
      <li>証紙購入が必要な県では販売所が限られていることもある</li>
      <li>診断書は発行まで日数を要するため、余裕をもって行動</li>
    </ul>

    <h3>補足：診断書が必要な県の一例</h3>
    <p>例：北海道、秋田県、奈良県、鹿児島県など。詳細は各県の公安委員会に事前確認が必要です。</p>

    <h3>よくある質問</h3>
    <dl>
      <dt>Q. 猟銃等講習会は年に何回開催されますか？</dt>
      <dd>都道府県によって異なりますが、年に数回（3〜6回程度）開催されることが一般的です。</dd>

      <dt>Q. 他県での受講は可能ですか？</dt>
      <dd>原則として居住地の都道府県での受講が推奨されますが、やむを得ない事情があれば他県での受講が認められる場合もあります。必ず事前に確認しましょう。</dd>

      <dt>Q. 診断書はどこで取得できますか？</dt>
      <dd>精神科や心療内科など、指定の様式に記入できる医療機関で取得します。警察署で指定医療機関を案内される場合もあります。</dd>

      <dt>Q. 写真はスピード写真でも大丈夫ですか？</dt>
      <dd>サイズや条件（正面・無帽・背景なしなど）を満たしていればスピード写真も可能ですが、規格外の場合は再提出となることがあります。</dd>

      <dt>Q. 申込は郵送でもできますか？</dt>
      <dd>郵送での受付を認めている都道府県もあります。ただし、提出期限・不備時の連絡に注意が必要です。</dd>

      <dt>Q. 申込書はネットでダウンロードできますか？</dt>
      <dd>一部の都道府県では公安委員会や警察の公式サイトからダウンロード可能です。そうでない場合は警察署で直接受け取る必要があります。</dd>

      <dt>Q. 診断書の有効期限はどのくらいですか？</dt>
      <dd>多くの県で発行から3か月以内のものが求められます。講習会の開催日を基準に計算される場合があるため注意が必要です。</dd>

      <dt>Q. 収入証紙はどこで買えますか？</dt>
      <dd>都道府県庁、出張所、金融機関、郵便局などで購入できますが、対応窓口は限られているため事前確認が重要です。</dd>

      <dt>Q. 講習会は平日ですか？土日もありますか？</dt>
      <dd>都道府県によっては土日に実施している場合もありますが、基本的には平日に開催されることが多いです。</dd>

      <dt>Q. 書類に不備があった場合はどうなりますか？</dt>
      <dd>受講を認められないこともあります。書類は提出前に警察署で確認してもらうのが安全です。</dd>
    </dl>

    <h4>※必ず最新の情報を各都道府県の公安委員会公式サイトや警察署で確認し、十分な余裕をもって申請を行いましょう。</h4>

